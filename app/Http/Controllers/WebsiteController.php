    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function search(Request $request)
    {
        // dd($request);
        // Search by Name and Product id
        $results = Application::where("name", "LIKE", '%'.$request->find.'%')->orWhere("id", "LIKE", '%'.$request->find.'%')->paginate(10);
        // $results->total();  // Parameter that sends the Total Amount result of paginate ()
        $results->withPath('/search?find='.$request->find);    // Parameter that sends the search URL for the following pages to pagination
        // dd($results);
        return view('website.applications.index', ['applications' => $results, 'search' => $request]);
    }
